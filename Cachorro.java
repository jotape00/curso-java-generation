package br.com.generation.atividade07;

public class Cachorro implements Animal {

	
	public void nome() {
		System.out.println("Nome: Geraldo");
	}

	
	public void idade() {
		System.out.println("Idade: 11 anos");
	}

	
	public void emitirSom() {
		System.out.println("Som: Au au au au!");
	}
	
	public void corre() {
		System.out.println("Sim, este cachorro corre muito!!");
	}
}
